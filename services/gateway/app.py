def get_redis():
    """Get Redis client"""
    global redis_client
    if redis_client is None:
        try:
            redis_client = redis.from_url(REDIS_URL)
        except Exception:
            pass
    return redis_client


def check_rate_limit(ip: str, endpoint: str, limit: int = 100, window: int = 60) -> tuple:
    """
    Check rate limit for IP + endpoint.
    Returns (allowed: bool, remaining: int, reset_time: int)
    """
    r = get_redis()
    if not r:
        return True, limit, int(time.time()) + window

    key = f"rate_limit:{ip}:{endpoint}"
    try:
        current = r.get(key)
        if current is None:
            r.setex(key, window, 1)
            return True, limit - 1, int(time.time()) + window

        current = int(current)
        if current >= limit:
            ttl = r.ttl(key)
            return False, 0, int(time.time()) + max(ttl, 0)

        r.incr(key)
        return True, limit - current - 1, int(time.time()) + r.ttl(key)
    except Exception:
        return True, limit, int(time.time()) + window


def get_rate_limit_rule(method: str, path: str):
    """Return the configured rate-limit rule for a request, if any."""
    return RATE_LIMIT_RULES.get((method, path))


def verify_jwt_token(token: str) -> dict:
    """Verify JWT token and return payload"""
    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=['HS256'])

        # Check if token is blacklisted
        r = get_redis()
        if r and r.exists(f"blacklist:{token}"):
            return None

        return payload
    except jwt.ExpiredSignatureError:
        return None
    except jwt.InvalidTokenError:
        return None


def create_csrf_token() -> str:
    """Create a short-lived CSRF token for browser clients."""
    payload = {
        'type': 'csrf',
        'iat': datetime.utcnow(),
        'exp': datetime.utcnow() + timedelta(hours=12),
    }
    return jwt.encode(payload, SECRET_KEY, algorithm='HS256')


def verify_csrf_token(token: str) -> bool:
    """Validate a CSRF token generated by the gateway."""
    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=['HS256'])
        return payload.get('type') == 'csrf'
    except jwt.InvalidTokenError:
        return False


def require_auth(f):
    """Decorator to require authentication"""
    @wraps(f)
    def decorated_function(*args, **kwargs):
        # Get token from Authorization header
        auth_header = request.headers.get('Authorization', '')
        if not auth_header.startswith('Bearer '):
            return jsonify({'error': 'Missing or invalid Authorization header'}), 401

        token = auth_header[7:]  # Remove 'Bearer ' prefix
        payload = verify_jwt_token(token)

        if not payload:
            return jsonify({'error': 'Invalid or expired token'}), 401

        # Store user info in request context
        g.user_id = payload.get('user_id')
        g.user_email = payload.get('email')
        g.user_name = payload.get('name')
        g.user_is_admin = bool(payload.get('is_admin'))

        return f(*args, **kwargs)
    return decorated_function


def require_admin(f):
    """Decorator to require an admin JWT token."""
    @wraps(f)
    def decorated_function(*args, **kwargs):
        auth_header = request.headers.get('Authorization', '')
        if not auth_header.startswith('Bearer '):
            return jsonify({'error': 'Missing or invalid Authorization header'}), 401

        token = auth_header[7:]
        payload = verify_jwt_token(token)
        if not payload:
            return jsonify({'error': 'Invalid or expired token'}), 401
        if not payload.get('is_admin'):
            return jsonify({'error': 'Admin access required'}), 403

        g.user_id = payload.get('user_id')
        g.user_email = payload.get('email')
        g.user_name = payload.get('name')
        g.user_is_admin = True
        return f(*args, **kwargs)
    return decorated_function


@app.before_request
def before_request():
    """Pre-request processing: logging, rate limiting"""
    g.request_id = str(uuid.uuid4())[:8]
    g.request_start_time = time.time()
    g.rate_limit = None

    rule = get_rate_limit_rule(request.method, request.path)
    if rule:
        allowed, remaining, reset_time = check_rate_limit(
            request.remote_addr,
            request.path,
            limit=rule['limit'],
            window=rule['window']
        )
        g.rate_limit = {
            'limit': rule['limit'],
            'remaining': remaining,
            'reset_time': reset_time,
        }

        if not allowed:
            response = jsonify({
                'error': 'Rate limit exceeded',
                'reset_time': reset_time,
                'retry_after': max(0, reset_time - int(time.time()))
            })
            response.status_code = 429
            response.headers['X-RateLimit-Limit'] = str(rule['limit'])
            response.headers['X-RateLimit-Remaining'] = '0'
            response.headers['X-RateLimit-Reset'] = str(reset_time)
            response.headers['Retry-After'] = str(max(0, reset_time - int(time.time())))
            return response

    if request.method in ('POST', 'PUT', 'DELETE') and request.path.startswith('/api/'):
        if request.path == '/api/csrf-token':
            return None

        csrf_token = request.headers.get('X-CSRF-Token', '')
        if not csrf_token or not verify_csrf_token(csrf_token):
            return jsonify({'error': 'Missing or invalid CSRF token'}), 403


@app.after_request
def after_request(response):
    """Post-request processing: headers, logging"""
    duration_ms = (time.time() - g.request_start_time) * 1000
    response.headers['X-Request-ID'] = g.request_id
    response.headers['X-Response-Time'] = f'{duration_ms:.2f}ms'
    response.headers['X-Service'] = 'api-gateway'

    # Security headers
    response.headers['X-Content-Type-Options'] = 'nosniff'
    response.headers['X-Frame-Options'] = 'DENY'
    response.headers['X-XSS-Protection'] = '1; mode=block'
    response.headers['Strict-Transport-Security'] = 'max-age=31536000; includeSubDomains'
    response.headers['Referrer-Policy'] = 'strict-origin-when-cross-origin'
    response.headers['Permissions-Policy'] = 'geolocation=(), microphone=(), camera=(), payment=(), usb=()'
    response.headers['Cross-Origin-Opener-Policy'] = 'same-origin'
    response.headers['Cross-Origin-Resource-Policy'] = 'same-origin'

    if getattr(g, 'rate_limit', None):
        response.headers['X-RateLimit-Limit'] = str(g.rate_limit['limit'])
        response.headers['X-RateLimit-Remaining'] = str(max(0, g.rate_limit['remaining']))
        response.headers['X-RateLimit-Reset'] = str(g.rate_limit['reset_time'])

    return response


