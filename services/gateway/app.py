def proxy_request(url: str, require_token: bool = False, require_admin: bool = False) -> Response:
    """Proxy a request to a backend service"""
    headers = {
        'X-Request-ID': g.request_id,
        'X-Forwarded-For': request.remote_addr,
    }

    # Forward guest cart identity so cart-service can load the correct cart.
    cart_id = request.headers.get('X-Cart-ID')
    if cart_id:
        headers['X-Cart-ID'] = cart_id

    # Forward authorization header if present and attach user context when valid.
    auth_header = request.headers.get('Authorization')
    payload = None
    if auth_header:
        headers['Authorization'] = auth_header

        if auth_header.startswith('Bearer '):
            token = auth_header[7:]
            payload = verify_jwt_token(token)
            if payload:
                headers['X-User-ID'] = str(payload.get('user_id', ''))
                headers['X-User-Email'] = payload.get('email', '')
                headers['X-User-Name'] = payload.get('name', '')
                headers['X-User-Is-Admin'] = 'true' if payload.get('is_admin') else 'false'

    if require_token:
        if not auth_header or not auth_header.startswith('Bearer '):
            return jsonify({'error': 'Missing or invalid Authorization header'}), 401
        if not payload:
            return jsonify({'error': 'Invalid or expired token'}), 401
    if require_admin and not (payload and payload.get('is_admin')):
        return jsonify({'error': 'Admin access required'}), 403

    # Forward query parameters
    params = request.args.to_dict()
    json_payload = request.get_json(silent=True)
    if json_payload is not None:
        headers['Content-Type'] = 'application/json'

    try:
        if request.method == 'GET':
            resp = requests.get(url, headers=headers, params=params, timeout=30)
        elif request.method == 'POST':
            resp = requests.post(url, headers=headers, params=params, json=json_payload, timeout=30)
        elif request.method == 'PUT':
            resp = requests.put(url, headers=headers, params=params, json=json_payload, timeout=30)
        elif request.method == 'DELETE':
            resp = requests.delete(url, headers=headers, params=params, timeout=30)
        else:
            return jsonify({'error': 'Method not allowed'}), 405

        # Forward response
        response = Response(
            resp.content,
            status=resp.status_code,
            headers={'Content-Type': 'application/json'}
        )
        return response

    except requests.RequestException as e:
        return jsonify({
            'error': 'Service unavailable',
            'message': str(e),
            'request_id': g.request_id
        }), 503


def proxy_file_upload(url: str, require_token: bool = False, require_admin: bool = False) -> Response:
    """Proxy multipart form-data uploads to a backend service."""
    headers = {
        'X-Request-ID': g.request_id,
        'X-Forwarded-For': request.remote_addr,
    }

    auth_header = request.headers.get('Authorization')
    payload = None
    if auth_header:
        headers['Authorization'] = auth_header
        if auth_header.startswith('Bearer '):
            payload = verify_jwt_token(auth_header[7:])
            if payload:
                headers['X-User-ID'] = str(payload.get('user_id', ''))
                headers['X-User-Email'] = payload.get('email', '')
                headers['X-User-Name'] = payload.get('name', '')
                headers['X-User-Is-Admin'] = 'true' if payload.get('is_admin') else 'false'

    if require_token:
        if not auth_header or not auth_header.startswith('Bearer '):
            return jsonify({'error': 'Missing or invalid Authorization header'}), 401
        if not payload:
            return jsonify({'error': 'Invalid or expired token'}), 401
    if require_admin and not (payload and payload.get('is_admin')):
        return jsonify({'error': 'Admin access required'}), 403

    upload = request.files.get('image')
    if not upload:
        return jsonify({'error': 'Image file is required'}), 400

    files = {
        'image': (upload.filename, upload.stream, upload.mimetype or 'application/octet-stream')
    }

    try:
        resp = requests.post(
            url,
            headers=headers,
            files=files,
            data=request.form.to_dict(),
            timeout=60
        )
        response = Response(
            resp.content,
            status=resp.status_code,
            headers={'Content-Type': resp.headers.get('Content-Type', 'application/json')}
        )
        return response
    except requests.RequestException as e:
        return jsonify({
            'error': 'Service unavailable',
            'message': str(e),
            'request_id': g.request_id
        }), 503


